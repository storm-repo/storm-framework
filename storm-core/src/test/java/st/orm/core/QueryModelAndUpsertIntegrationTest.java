import st.orm.PK;
import st.orm.PersistenceException;
import st.orm.core.model.City;
import st.orm.core.template.ORMTemplate;

/**
 * Integration tests targeting uncovered branches in:
 * - QueryModelImpl.resolveElements (Record-next-to-operator, Stream value, etc.)
 * - EntityRepositoryImpl.upsert (upsert-update routing, SQL-level upsert failure)
 * - EntityRepositoryImpl.upsertAndFetchId (same routing)
 * - EntityRepositoryImpl.upsertAndFetchIds batch (UpsertUpdateKey, UpsertNoOp paths)
 * - EntityRepositoryImpl.validateUpsert (non-auto-generated PK with default value)
 * - EntityRepositoryImpl.batch update and updateAndFetch with callbacks
 * - EntityRepositoryImpl.stream-based insert, upsert, delete, deleteByRef
 */
@SuppressWarnings("ALL")
@ExtendWith(SpringExtension.class)
@ContextConfiguration(classes = IntegrationConfig.class)
@DataJpaTest(showSql = false)
public class QueryModelAndUpsertIntegrationTest {

    @Autowired
    private DataSource dataSource;

    // EntityRepositoryImpl: upsert with entity callback (routes to update)

    @Test
    public void testUpsertExistingEntityWithCallback() {
        ORMTemplate baseOrm = of(dataSource);
        java.util.concurrent.atomic.AtomicInteger beforeUpdateCount = new java.util.concurrent.atomic.AtomicInteger();
        java.util.concurrent.atomic.AtomicInteger afterUpdateCount = new java.util.concurrent.atomic.AtomicInteger();
        ORMTemplate orm = baseOrm.withEntityCallback(new st.orm.EntityCallback<City>() {
            @Override public City beforeUpdate(City entity) {
                beforeUpdateCount.incrementAndGet();
                return entity;
            }
            @Override public void afterUpdate(City entity) {
                afterUpdateCount.incrementAndGet();
            }
        });
        var cities = orm.entity(City.class);
        City inserted = cities.insertAndFetch(new City(null, "CallbackUpsertCity"));

        cities.upsert(new City(inserted.id(), "CallbackUpsertCityUpdated"));
        assertEquals(1, beforeUpdateCount.get());
        assertEquals(1, afterUpdateCount.get());
    }

    // EntityRepositoryImpl: batch upsert with callback (routes to UpsertUpdateKey + callbacks)

    @Test
    public void testBatchUpsertAndFetchIdsWithCallback() {
        ORMTemplate baseOrm = of(dataSource);
        java.util.concurrent.atomic.AtomicInteger beforeCount = new java.util.concurrent.atomic.AtomicInteger();
        java.util.concurrent.atomic.AtomicInteger afterCount = new java.util.concurrent.atomic.AtomicInteger();
        ORMTemplate orm = baseOrm.withEntityCallback(new st.orm.EntityCallback<City>() {
            @Override public City beforeUpdate(City entity) {
                beforeCount.incrementAndGet();
                return entity;
            }
            @Override public void afterUpdate(City entity) {
                afterCount.incrementAndGet();
            }
        });
        var cities = orm.entity(City.class);
        City city1 = cities.insertAndFetch(new City(null, "BatchCallbackCity1"));
        City city2 = cities.insertAndFetch(new City(null, "BatchCallbackCity2"));

        List<Integer> ids = cities.upsertAndFetchIds(
                List.of(new City(city1.id(), "BatchCallbackCity1Updated"),
                         new City(city2.id(), "BatchCallbackCity2Updated")));
        assertEquals(2, ids.size());
        assertTrue(beforeCount.get() >= 2);
        assertTrue(afterCount.get() >= 2);
    }

    // EntityRepositoryImpl: validateUpsert with NONE generation

    @DbTable("pet_type")
    record PetTypeNoneGeneration(
            @PK(generation = st.orm.GenerationStrategy.NONE) Integer id,
            String name
    ) implements Entity<Integer> {}

    @Test
    public void testUpsertWithNonAutoGeneratedPKAndDefaultPKThrows() {
        var orm = of(dataSource);
        var petTypes = orm.entity(PetTypeNoneGeneration.class);
        assertThrows(PersistenceException.class,
                () -> petTypes.upsert(new PetTypeNoneGeneration(null, "TestType")));
    }

    @Test
    public void testUpsertAndFetchIdWithNonAutoGeneratedPKAndDefaultPKThrows() {
        var orm = of(dataSource);
        var petTypes = orm.entity(PetTypeNoneGeneration.class);
        assertThrows(PersistenceException.class,
                () -> petTypes.upsertAndFetchId(new PetTypeNoneGeneration(null, "TestType")));
    }

    // QueryModelImpl.resolveElements: Record next to operator should throw

    @Test
    public void testRecordNextToOperatorThrows() {
        var orm = of(dataSource);
        City city = new City(1, "Test");
        assertThrows(PersistenceException.class, () ->
                orm.query(raw("SELECT * FROM city WHERE id = \0", city)).getResultList(City.class));
    }

    @Test
    public void testRecordNextToLeadingOperatorThrows() {
        var orm = of(dataSource);
        City city = new City(1, "Test");
        assertThrows(PersistenceException.class, () ->
                orm.query(raw("SELECT * FROM city WHERE \0 = 1", city)).getResultList(City.class));
    }

    // QueryModelImpl: Stream as value should throw

    @Test
    public void testStreamAsTemplateValueThrows() {
        var orm = of(dataSource);
        assertThrows(PersistenceException.class, () ->
                orm.query(raw("SELECT * FROM city WHERE id = \0", java.util.stream.Stream.of(1)))
                        .getResultList(City.class));
    }

    // EntityRepositoryImpl: batch update with callback

    @Test
    public void testBatchUpdateWithCallback() {
        ORMTemplate baseOrm = of(dataSource);
        java.util.concurrent.atomic.AtomicInteger count = new java.util.concurrent.atomic.AtomicInteger();
        ORMTemplate orm = baseOrm.withEntityCallback(new st.orm.EntityCallback<City>() {
            @Override public City beforeUpdate(City entity) {
                count.incrementAndGet();
                return entity;
            }
        });
        var cities = orm.entity(City.class);
        City city1 = cities.insertAndFetch(new City(null, "BatchUpdateCb1"));
        City city2 = cities.insertAndFetch(new City(null, "BatchUpdateCb2"));

        cities.update(List.of(
                new City(city1.id(), "BatchUpdateCb1Updated"),
                new City(city2.id(), "BatchUpdateCb2Updated")));
        assertTrue(count.get() >= 2);
    }

    // EntityRepositoryImpl: batch updateAndFetch with callback

    @Test
    public void testBatchUpdateAndFetchWithCallback() {
        ORMTemplate baseOrm = of(dataSource);
        java.util.concurrent.atomic.AtomicInteger count = new java.util.concurrent.atomic.AtomicInteger();
        ORMTemplate orm = baseOrm.withEntityCallback(new st.orm.EntityCallback<City>() {
            @Override public City beforeUpdate(City entity) {
                count.incrementAndGet();
                return entity;
            }
        });
        var cities = orm.entity(City.class);
        City city1 = cities.insertAndFetch(new City(null, "BatchUFCb1"));
        City city2 = cities.insertAndFetch(new City(null, "BatchUFCb2"));

        List<City> result = cities.updateAndFetch(List.of(
                new City(city1.id(), "BatchUFCb1Updated"),
                new City(city2.id(), "BatchUFCb2Updated")));
        assertEquals(2, result.size());
        assertTrue(count.get() >= 2);
    }
}
